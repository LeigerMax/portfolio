// Fonction utilitaire pour obtenir le contenu traduit
export function getTranslatedContent(
  contentType: 'projects' | 'experiences' | 'education' | 'certificates' | 'personal',
  id: string,
  language: Language,
  field: string
): string {
  const contentMap = {
    projects: projectsContent,
    experiences: experiencesContent,
    education: educationContent,
    certificates: certificatesContent,
    personal: personalContent
  } as const;

  const content = contentMap[contentType];

  if (contentType === 'personal') {
    // personalContent is an object mapping language -> { bio: string; philosophy: string }
    const personal = content as Record<Language, Record<string, string>>;
    return personal[language]?.[field] || '';
  }

  // Other content types are objects mapping language -> id -> field -> string
  const generic = content as Record<Language, Record<string, Record<string, string>>>;
  return generic[language]?.[id]?.[field] || '';
}